# Week 1 - Functions and Variables Practice

# Variable examples
name = "Philip"
age = 25
height = 178  # in cm

print("Name:", name)
print("Age:", age)
print("Height:", height, "cm")


# Function example
def greet(person):
    return f"Hello, {person}! Welcome to AI-2026."


# Function with calculation
def add_numbers(a, b):
    return a + b


# Calling the functions
print(greet(name))

result = add_numbers(10, 5)
print("Addition result:", result)